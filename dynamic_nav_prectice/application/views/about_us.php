                        <h1><i class="fas fa-hand-point-right"></i> Overview</h1>
                        <p>After a long struggle and a relentless movement, the Shahjalal University of Science and Technology (SUST) was established in 1986. The only university of its kind at that time, it started it's career on the 1st Phalgun(13th of February 1991) with only three departments: Physics, Chemistry and Economics, 13 teachers and 205 students. It has now expanded to 7 schools, 27 departments and two institutes. The number of teachers has grown to 552 and the students to 10922. Besides, the University has 13 affiliated medical colleges under the School of Medical Sciences with 4000 students. SUST introduced the integrated honours course for the first time in Bangladesh. It introduced the semester system from the 1996-97 session and there was a remarkable improvement in the quality of education after the introduction of this system, which was visible even in the national arena. Every student from SUST has to take two language courses, one in Bangla and the other one in English. Every student also has to take two computer courses, one for computer literacy and the other to learn a computer language. The computer center of SUST offers courses to every employees and one can say, without any hesitation that SUST is the most IT enabled university in this country. SUST has started it's graduate programs by offering masters degree to graduating bachelors of most of the departments. It is difficult to start a world-class research program without a well funded graduate school, even then the teachers of SUST are trying to create a research environment in the campus. Two research journals are published regularly from SUST, one in Bangla and the other one in English where a few hundred research papers are submitted every year. Several departments have hosted International Conferences and are planning to organize similar events in the near future.</p>
